                          class WeightConversion:
                            @staticmethod
                            def g_to_kg(g):
                                return g / 1000

                            @staticmethod
                            def kg_to_g(kg):
                                return kg * 1000

                            @staticmethod
                            def g_to_lb(g):
                                return g / 453.59237

                            @staticmethod
                            def lb_to_g(lb):
                                return lb * 453.59237

                            @staticmethod
                            def g_to_oz(g):
                                return g / 28.34952

                            @staticmethod
                            def oz_to_g(oz):
                                return oz * 28.34952

                            @staticmethod
                            def kg_to_lb(kg):
                                return kg * 2.20462

                            @staticmethod
                            def lb_to_kg(lb):
                                return lb / 2.20462

                            @staticmethod
                            def kg_to_oz(kg):
                                return kg * 35.274

                            @staticmethod
                            def oz_to_kg(oz):
                                return oz / 35.274

                            @staticmethod
                            def lb_to_oz(lb):
                                return lb * 16

                            @staticmethod
                            def oz_to_lb(oz):
                                return oz / 16